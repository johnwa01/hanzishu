class School {
  int id = -1;
  String code = "";
  String teacherName = "";
  String schoolName = "";
  int countryId = -1;

  School(
      int id,
      String code,
      String teacherName,
      String schoolName,
      int countryId)
  {
    this.id = id;
    this.code = code;
    this.teacherName = schoolName;
    this.teacherName = schoolName;
    this.countryId = countryId;
  }
}